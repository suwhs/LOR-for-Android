package io.github.getsmp.lorforandroid.ui.section.forum.section;

public class ForumSection {
    private String url;
    private String name;

    public ForumSection(String url, String name) {
        this.url = url;
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public String getName() {
        return name;
    }
}
